package data_structures.chapter4_trees_n_graphs.amazon_igotanoffer;

public class ListNode {
    public int val;
    public  ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
